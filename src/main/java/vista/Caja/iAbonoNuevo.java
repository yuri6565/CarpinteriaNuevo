    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // Validación de campos
        if (dateFecha.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Seleccione la fecha", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Eliminar comas para validación numérica
        String importeText = txtImporte.getText().replace(",", "");
        if (importeText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el importe", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            Long.parseLong(importeText); // Usamos Long en lugar de Double
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Importe debe ser numérico", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (cmbMetodo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un método de pago", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        guardado = true;
        dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

