    private void formatNumberField() {
        // Eliminar todas las comas existentes para procesar el número limpio
        String text = txtImporte.getText().replaceAll("[^0-9]", "");

        if (!text.isEmpty()) {
            try {
                long amount = Long.parseLong(text);
                // Formatear con comas como separadores de miles
                String formatted = String.format("%,d", amount)
                        .replace(",", "X") // Marcador temporal
                        .replace(".", ",") // Cambiar puntos por comas (si el locale usa puntos)
                        .replace("X", ","); // Restaurar comas de miles

                // Solo actualizar si el formato cambió (para evitar bucles)
                if (!formatted.equals(txtImporte.getText())) {
                    txtImporte.setText(formatted);
                    // Mover el cursor al final
                    txtImporte.setCaretPosition(formatted.length());
                }
            } catch (NumberFormatException e) {
                // No hacer nada si no es número válido
            }
        }
    }

