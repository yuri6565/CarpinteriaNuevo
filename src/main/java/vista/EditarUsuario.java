    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
       try {
    String id_usuarioStr = txtCodigo.getText();
    if (id_usuarioStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese un ID válido", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int id_usuario = Integer.parseInt(id_usuarioStr);  // Cambiado el nombre de la variable para coincidir
    Connection con = Conexion.getConnection();
    String sql = "SELECT * FROM usuario WHERE id_usuario = ?";
    PreparedStatement ps = con.prepareStatement(sql);
    ps.setInt(1, id_usuario);  // Ahora usa la variable correcta

    ResultSet rs = ps.executeQuery();
    if (rs.next()) {
        // Llenar los campos con los datos de la base de datos
        txtCodigo.setText(String.valueOf(rs.getInt("id_usuario")));
        txtNombre.setText(rs.getString("nombre"));
        txtUsuario.setText(rs.getString("usuario"));
        txtCorreo.setText(rs.getString("correo_electronico"));
        txtContraseña.setText(rs.getString("contraseña"));
        txtRol.setText(rs.getString("rol"));
    } else {
        JOptionPane.showMessageDialog(this, "No se encontró usuario con ese ID", "Error", JOptionPane.ERROR_MESSAGE);
    }

    con.close();
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "El ID debe ser un número", "Error", JOptionPane.ERROR_MESSAGE);
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error al buscar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    e.printStackTrace();
}
    }//GEN-LAST:event_btnBuscaActionPerformed

