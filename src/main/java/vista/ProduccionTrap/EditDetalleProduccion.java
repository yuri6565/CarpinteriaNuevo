    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        try {
            String idStr = txtBusca.getText();
            if (idStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ingrese un ID vÃ¡lido", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int id = Integer.parseInt(idStr);
            Connection con = Conexion.getConnection();
            String sql = "SELECT * FROM detalle_produccion WHERE idetalle_produccion = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, id);

            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                idProduccionActual = id;
                // Llenar los campos con los datos de la base de datos
                txtmaterial.setText(rs.getString("materiales")); // AsegÃºrate que este campo exista en tu tabla
                txtdimension.setText(rs.getString("dimensiones")); // AsegÃºrate que este campo exista en tu tabla
                txtcantidad.setText(String.valueOf(rs.getInt("cantidad"))); // AsegÃºrate que este campo exista en tu tabla

                // Puedes agregar mÃ¡s campos segÃºn necesites
            } else {
                JOptionPane.showMessageDialog(this, "No se encontrÃ³ producciÃ³n con ese ID", "Error", JOptionPane.ERROR_MESSAGE);
            }

            con.close();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un nÃºmero", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnBuscaActionPerformed

